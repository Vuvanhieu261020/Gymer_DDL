    private void jLabel5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel5MouseClicked
        // TODO add your handling code here:
        Employee temp = new Employee();
        this.count++;
        if (this.count % 2 != 0) {
            sex.setEditable(true);
            BoxVitri.setEditable(true);
            Ten.setText("");
            MaNV.setText(KeyDB.genKey());
            CMND.setText("");
            diachi.setText("");
            sex.getEditor().setItem("Nam");
            BoxVitri.getEditor().setItem("Nhân viên");
            namsinh.setText(DateTime.getTimeFormat2().substring(6));
            sdt.setText("");
        }
        else {
            if (
                Ten.getText().equals("") ||
                CMND.getText().equals("") ||
                diachi.getText().equals("") ||
                namsinh.getText().equals("") ||
                sdt.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "Bạn chưa nhập gì cả xin hãy kiểm tra lại !");
            }
            else {
                setDatatoEnity(temp);
                String ID = JOptionPane.showInputDialog("Nhập User Name");
                String Password = JOptionPane.showInputDialog("Nhập Password");
                if (eqi.insert(temp,ID,Password)){
                    JOptionPane.showMessageDialog(null, "Thêm thành công");
                    showData(eqi.getAll());
                }
                else JOptionPane.showMessageDialog(null, "Có lỗi trong quá trình thêm có thể user name đã được sử dụng hoặc lỗi hệ thống");
            }
        }
    }//GEN-LAST:event_jLabel5MouseClicked

